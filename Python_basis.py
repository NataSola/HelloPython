# Строчные / прописные

name = 'иван иваныч иванов'
print (name)

# первые заглавные
name = name.title()  
print (name)

# все заглавные
name = name.upper()
print (name)

# все прописные
# предпочительный вариант: перед сохранением данных, всё перевести в нижний регистр. Перед выводом использовать нужный регистр
name = name.lower()
print (name)